int WortCount()
{
	return sizeof(Worte) / sizeof(LPWORD);
}


LPWORD GimmeOne()
{
	static int first = 1;

	if (first)
	{
		SYSTEMTIME st;
		FILETIME ft;
		first = 0;
		GetLocalTime(&st);
		SystemTimeToFileTime(&st, &ft);
		srand(ft.dwLowDateTime ^ ft.dwHighDateTime ^ GetTickCount());
	}

	return Worte[RNDNR(WortCount())];
}


